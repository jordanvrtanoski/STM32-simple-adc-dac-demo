/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{
  /* init code for LWIP */
  MX_LWIP_Init();
  /* USER CODE BEGIN 5 */
  char *message = malloc(1024);
  int msglen = 0;
  RTC_TimeTypeDef time;
  RTC_DateTypeDef date;

  // Initialize the SNMP Agent
  SNMP_Info snmp_info;
  snmp_info.syscontact = (u8_t*)"admin";
  snmp_info.syslocation = (u8_t*)"DevLab";
  snmp_info.sysdescr = (u8_t*)"My test device";
  SNMP_agent_init_with_info(&snmp_info);

  osDelay(1000);

  HAL_RTC_Init(&hrtc);

  ip_addr_t PC_IPADDR;
  IP_ADDR4(&PC_IPADDR, 10, 0, 1, 160);

  struct udp_pcb* my_udp = udp_new();
  udp_connect(my_udp, &PC_IPADDR, 55151);
  struct pbuf* udp_buffer = NULL;

  /* Infinite loop */
  for (;;) {
	HAL_GPIO_TogglePin(GPIOB, LD1_Pin);
    osDelay(1000);
    HAL_RTC_GetTime(&hrtc, &time, RTC_FORMAT_BIN);
    HAL_RTC_GetDate(&hrtc, &date, RTC_FORMAT_BIN);
    adcVal = Read_ADC_Value(&hadc1);

    msglen = snprintf(message, 1024, "[20%02u-%02u-%02u %02u:%02u:%02u] (%lu) = %lu \n\r", date.Year, date.Month, date.Date, time.Hours, time.Minutes, time.Seconds, dacVal, adcVal);
    /* !! PBUF_RAM is critical for correct operation !! */
    udp_buffer = pbuf_alloc(PBUF_TRANSPORT, msglen, PBUF_RAM);

    if (udp_buffer != NULL) {
      memcpy(udp_buffer->payload, message, msglen);
      udp_send(my_udp, udp_buffer);
      pbuf_free(udp_buffer);
    }
  }
  /* USER CODE END 5 */
}

