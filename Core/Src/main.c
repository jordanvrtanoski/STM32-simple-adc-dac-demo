/**
* @brief Function implementing the t_Console thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_tConsole */
void tConsole(void const * argument)
{
  /* USER CODE BEGIN tConsole */
  /* Infinite loop */
  uint8_t pNewLine[] = "\n\r";
  uint8_t pValidCommand[] = "\e[32mValid Command\e[0m";
  uint8_t pWrongCommand[] = "\e[31mInvalid command\e[0m";
  uint8_t cmdBuff[1024];
  uint8_t* argv[32];
  int count=0;

  CONSOLE_CMD_init();

  for(;;)
  {
	  // Wait for user input
	  count = readCommand(&huart3, cmdBuff, sizeof(cmdBuff));
	  // Tokenize the user input
	  count = tokenizeCommand(cmdBuff, count, (uint8_t **)&argv, 32);
	  // Check how many tokens we had received
	  if (count > 0) {
		  // We have captured a command in the buffer, search for the command in the list
		  cCommand *cmd = findCommand(argv[0]);
		  if ( cmd != NULL) {
			  // If command is found, execute the command
			  HAL_UART_Transmit(&huart3, pNewLine, sizeof(pNewLine),1000);
			  cmd->callback(&huart3, argv, count);
		  } else {
			  // Command not found
			  HAL_UART_Transmit(&huart3, pNewLine, sizeof(pNewLine),1000);
			  HAL_UART_Transmit(&huart3, pWrongCommand, sizeof(pWrongCommand),1000);
		  }
	  }
  }
  /* USER CODE END tConsole */
}

